CREATE FUNCTION dbo.GET_BIRTHDATE(@EGN CHAR(10))
RETURNS date
AS
BEGIN
	DECLARE @year INT = 1900 + 10*SUBSTRING(@EGN, 1, 1) + SUBSTRING(@EGN, 2, 1);
	DECLARE @month INT = 10*SUBSTRING(@EGN, 3, 1) + SUBSTRING(@EGN, 4, 1);
	DECLARE @day INT = 10*SUBSTRING(@EGN, 5, 1) + SUBSTRING(@EGN, 6, 1);
	IF @month > 40
	BEGIN
		SET @month = @month - 40;
		SET @year = @year + 100;
	END
	ELSE IF @month > 20
	BEGIN
		SET @month = @month - 20;
		SET @year = @year - 100;
	END;
	RETURN DATEFROMPARTS(@year, @month, @day);
END;
GO

CREATE FUNCTION dbo.CHECKDIGIT(@EGN CHAR(10))
RETURNS int
AS
BEGIN
	DECLARE @sum int = 0;
	DECLARE @i int = 9;
	WHILE @i >= 1
	BEGIN
		SET @sum = (@sum + SUBSTRING(@EGN, @i, 1)) * 2;
		SET @i = @i - 1;
	END;
	RETURN @sum % 11 % 10;
END;
GO

----- Create Tables -----

CREATE TABLE PUBLISHER (
	NAME VARCHAR(63) PRIMARY KEY,
	ADDRESS VARCHAR(255) NOT NULL,
	PHONE_NUMBER VARCHAR(10) NOT NULL CONSTRAINT pub_ph_num_digits_only CHECK (PHONE_NUMBER NOT LIKE '%[^0-9]%') 
);

CREATE TABLE AUTHOR (
	NAME VARCHAR(63) PRIMARY KEY,
	BIRTHDATE DATE NOT NULL,
	GENDER CHAR(1) NOT NULL CHECK (GENDER IN ('M','F')),
	NATIONALITY VARCHAR(63) NOT NULL 
);

CREATE TABLE BOOK (
	ISBN VARCHAR(13) PRIMARY KEY CONSTRAINT isbn_digits_only CHECK (ISBN NOT LIKE '%[^0-9]%'),
    TITLE VARCHAR(255) NOT NULL,
	AUTHOR_NAME VARCHAR(63) NOT NULL FOREIGN KEY REFERENCES AUTHOR(NAME),
	YEAR INTEGER NOT NULL,
	LANGUAGE VARCHAR(2) NOT NULL,
    GENRE CHAR(31) NOT NULL,
	PUBLISHER_NAME VARCHAR(63) NOT NULL FOREIGN KEY REFERENCES PUBLISHER(NAME),
	PAGES_NUMBER INTEGER NOT NULL
);

CREATE TABLE MEMBER (
    NAME VARCHAR(63) NOT NULL,
	EGN CHAR(10) PRIMARY KEY CONSTRAINT mem_egn_check CHECK (EGN NOT LIKE '%[^0-9]%' AND dbo.CHECKDIGIT(EGN) = SUBSTRING(EGN, 10, 1) AND dbo.GET_BIRTHDATE(EGN) <= GETDATE()),
    ADDRESS VARCHAR(255) NOT NULL,
	PHONE_NUMBER VARCHAR(10) NOT NULL CONSTRAINT mem_ph_num_digits_only CHECK (PHONE_NUMBER NOT LIKE '%[^0-9]%')
);

CREATE TABLE DELETED_MEMBER (
    NAME VARCHAR(63) NOT NULL,
	EGN CHAR(10) PRIMARY KEY CONSTRAINT mem_del_egn_check CHECK (EGN NOT LIKE '%[^0-9]%' AND dbo.CHECKDIGIT(EGN) = SUBSTRING(EGN, 10, 1) AND dbo.GET_BIRTHDATE(EGN) <= GETDATE()),
    ADDRESS VARCHAR(255) NOT NULL,
	PHONE_NUMBER VARCHAR(10) NOT NULL CONSTRAINT mem_del_ph_num_digits_only CHECK (PHONE_NUMBER NOT LIKE '%[^0-9]%')
);

CREATE TABLE LIBRARIAN (
    NAME VARCHAR(63) NOT NULL,
	EGN CHAR(10) PRIMARY KEY CONSTRAINT lib_egn_check CHECK (EGN NOT LIKE '%[^0-9]%' AND dbo.CHECKDIGIT(EGN) = SUBSTRING(EGN, 10, 1) AND dbo.GET_BIRTHDATE(EGN) <= GETDATE()),
    ADDRESS VARCHAR(255) NOT NULL,
	SECTOR VARCHAR(31) NOT NULL
);

CREATE TABLE BOOKID (
    ISBN VARCHAR(13) NOT NULL FOREIGN KEY REFERENCES BOOK (ISBN),
	ID INTEGER IDENTITY(1,1) PRIMARY KEY
);

CREATE TABLE BORROWING (
	BOOK_ID INTEGER NOT NULL FOREIGN KEY REFERENCES BOOKID(ID),
	MEMBER_EGN CHAR(10) NOT NULL FOREIGN KEY REFERENCES MEMBER(EGN) ON DELETE CASCADE,
	BORROW_DATE DATE NOT NULL,
	DUE_DATE DATE NOT NULL,
	RETURN_DATE DATE,
	LIBRARIAN_EGN CHAR(10) NOT NULL FOREIGN KEY REFERENCES LIBRARIAN(EGN)
);
GO

